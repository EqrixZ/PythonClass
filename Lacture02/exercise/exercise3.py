#input the test score
Test_score = int(input("Enter your test score: "))
#if elif function
if Test_score >=90:
    print ("Your grade is A.")
elif Test_score >=80:
    print ("Your grade is B.")
elif Test_score >=70:
    print ("Your grade is C.")
elif Test_score >=60:
    print ("Your grade is D.")
else:
    print("Your grade is F.")
    